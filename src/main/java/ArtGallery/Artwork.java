package ArtGallery;

public class Artwork {

    private String title;
    private String artist;
    private String nft;
    private double price;

    public Artwork(String title, String artist,String nft,int price){
        this.title = title;
        this.artist = artist;
        this.nft = nft;
        this.price = price;
    }

    public int getPrice(){
        return getPrice();}




}
